def get_tw50_stocks() -> List[str]:
    """
    取得台股 50 成分股清單
    
    Returns:
        股票代碼清單（Yahoo Finance 格式）
    """
    return TW50_STOCKS.copy()


def get_tw50_names() -> Dict[str, str]:
    """
    取得台股 50 名稱對照表
    
    Returns:
        股票代碼 -> 名稱 的對照表
    """
    return TW50_NAMES.copy()


def get_stock_name(code: str) -> str:
    """
    根據股票代碼取得名稱
    
    Args:
        code: 股票代碼（如 2330.TW）
        
    Returns:
        股票名稱，如果找不到則返回代碼本身
    """
    return TW50_NAMES.get(code, code)


